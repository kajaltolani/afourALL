#Python program to do all mathematical operation with two variables provided by the user.

X=int (input('enter data for var1 = X: '))
print('X: ',X)
Y=int (input('enter data for var2 = Y: '))
print('Y: ',Y)
print('Addition X+Y: ',X+Y)
print('Subtraction X-Y: ',X-Y)
print('Division X/Y: ',X/Y)
print('Multiplication X*Y: ',X*Y)
print('Quotient X//Y: ',X//Y)
print('Remainder X%Y: ',X%Y)
print('Power X**Y: ',X**Y)
